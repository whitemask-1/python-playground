#When writing Python code, errors are commonand can occur for various reasons
#Understanding them is the key to debugging quickly and effectively
#Messages will tell you exactly what went wrong and where to look in your code if you know how to read them

#Here are some common error types in Python:
#SyntaxError - Occurs when the code is not written in proper Python syntax
#NameError - Happens when a variable or function name is not found
#TypeError - Raised when an operation is performed on an incompatible data type
#IndexError - Occurs when trying to access an index that is out of range for a
#AttributeError - Happens when trying to access an attribute or method that does not exist for an object

#Example of a SyntaxError
# print("Hello, World!"  # Missing closing parenthesis

#Example of a NameError
# print(undeclared_variable) # Variable not defined

#Example of a TypeError
# result = '5' + 10  # Cannot add string and integer

#Example of an IndexError
# my_list = [1, 2, 3]
# print(my_list[5])  # Index out of range

